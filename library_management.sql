    SELECT MEMBER_ID
    FROM TRANSACTIONS
);

-- ========== 9. IMPLEMENT DATE & TIME FUNCTIONS ==========  

-- * EXTRACT THE YEAR FROM PUBLISHED_DATE TO COUNT BOOKS BY PUBLICATION YEAR.
SELECT YEAR(PUBLISHED_DATE) AS PUBLICATION_YEAR, 
    COUNT(BOOK_ID) AS TOTAL_BOOKS
FROM BOOKS
GROUP BY PUBLICATION_YEAR;

-- * FIND THE DIFFERENCE IN YEAR BETWEEN BORROW_DATE AND RETURN_DATE TO CALCULATE LATE RETURN FINES.
SELECT TRANSACTION_ID,
       MEMBER_ID,
       BOOK_ID,
       BORROW_DATE,
       RETURN_DATE,
       DATEDIFF(RETURN_DATE, BORROW_DATE) AS DAYS_DIFF
FROM TRANSACTIONS;

-- * FORMAT BORROW_DATE AS DD-MM-YYYY.
SELECT TRANSACTION_ID,
       MEMBER_ID,
       BOOK_ID,
       DATE_FORMAT(BORROW_DATE, '%d-%m-%Y') AS BORROW_DATE_FORMATTED,
       DATE_FORMAT(RETURN_DATE, '%d-%m-%Y') AS RETURN_DATE_FORMATTED
FROM TRANSACTIONS;


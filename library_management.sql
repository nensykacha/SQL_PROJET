-- ========== 7. IMPLEMENT JOINS ========== 

-- * RETRIEVE A LIST OF BOOKS ALONG WITH THEIR RESPECTIVE AUTHORS NAMES USING INNER JOIN.
SELECT B.TITLE, A.NAME AS AUTHOR_NAME
FROM BOOKS B
INNER JOIN AUTHORS A 
ON B.AUTHOR_ID = A.AUTHOR_ID;

-- * GET DETAILS OF MEMBERS WHO HAVE BORROWED BOOKS USING LEFT JOIN.
SELECT M.NAME AS MEMBER_NAME,
    T.BOOK_ID, 
    T.BORROW_DATE
FROM MEMBERS M
LEFT JOIN TRANSACTIONS T
ON M.MEMBER_ID = T.MEMBER_ID
WHERE T.BOOK_ID IS NOT NULL;

-- * FIND BOOKS THAT HAVEN'T BEEN BORROWED USING RIGHT JOIN.
SELECT B.TITLE AS BOOK_TITLE
FROM BOOKS B
LEFT JOIN TRANSACTIONS T
ON B.BOOK_ID = T.BOOK_ID
WHERE T.BOOK_ID IS NULL;

-- * SHOW MEMBERS WHO HAVE NEVER BORROWED A BOOK USING FULL OUTER JOIN.
    -- Note: MySQL does not support FULL OUTER JOIN directly,
    -- so it is implemented using LEFT JOIN and RIGHT JOIN with UNION.
SELECT M.NAME, T.BOOK_ID
FROM MEMBERS M
LEFT JOIN TRANSACTIONS T ON M.MEMBER_ID = T.MEMBER_ID

UNION

SELECT M.NAME, T.BOOK_ID
FROM MEMBERS M
RIGHT JOIN TRANSACTIONS T ON M.MEMBER_ID = T.MEMBER_ID;


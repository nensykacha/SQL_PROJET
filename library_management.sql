-- ========== 11. IMPLEMENT WINDOW FUNCTIONS ==========

-- * RANK BOOKS BASED ON NUMBER OF TIMES THEY HAVE BEEN BORROWED.
SELECT BOOK_ID,
        COUNT(BOOK_ID) AS BORROW_COUNT,
        RANK() OVER(ORDER BY COUNT(BOOK_ID) DESC) AS BORROW_RANK
FROM TRANSACTIONS
GROUP BY BOOK_ID;

-- * SHOW THE CUMULATIVE NUMBER OF BOOKS BORROWED PER NUMBER.
SELECT MEMBER_ID,
    BORROW_COUNT,
    SUM(BORROW_COUNT) OVER(ORDER BY MEMBER_ID) AS CUMULATIVE_BORROW
    FROM (
        SELECT MEMBER_ID, COUNT(BOOK_ID) AS BORROW_COUNT
        FROM TRANSACTIONS
        GROUP BY MEMBER_ID
    ) AS TEMP
ORDER BY MEMBER_ID;

-- * DISPLAY THE MOVING AVG OF THE BOOKS BORROWED IN THE LAST 3 MONTHS.
SELECT BORROW_DATE,
    BORROW_COUNT,
    AVG(BORROW_COUNT) OVER(ORDER BY BORROW_DATE ROWS BETWEEN 2 PRECEDING AND CURRENT ROW) AS MOVING_AVG
    FROM (
        SELECT BORROW_DATE, COUNT(BOOK_ID) AS BORROW_COUNT
        FROM TRANSACTIONS
        GROUP BY BORROW_DATE
    ) AS TEMP
ORDER BY BORROW_DATE;

-- ========== 12. APPLY CASE EXPRESSION ==========

-- * ASSIGN A MEMBERSHIP_STATUS COLUMN WITH VALUES:
    -- - 'ACTIVE' IF MEMBER HAS BORROWED A BOOK IN LAST 6 MONTHS.
    -- - 'INACTIVE' IF MEMBER HAS NOT BORROWED A BOOK IN LAST 6 MONTHS.
SELECT MEMBER_ID,
        NAME,
        CASE 
             WHEN MEMBER_ID IN(
                 SELECT MEMBER_ID
                 FROM TRANSACTIONS
                 WHERE BORROW_DATE >= DATE_SUB(CURDATE(), INTERVAL 6 MONTH)
             ) THEN 'ACTIVE'
             ELSE 'INACTIVE'
        END AS MEMBERSHIP_STATUS
FROM MEMBERS;

-- * CATEGORIZE BOOKS:
    -- - 'NEW_ARRIVAL' IF THE BOOK PUBLISHED AFTER 2020.
    -- - 'CLASSIC' IF THE BOOK PUBLISHED BEFORE 2000.
    -- - 'REGULAR' FOR ALL OTHER BOOKS.
SELECT BOOK_ID,
        TITLE,
        CASE 
             WHEN PUBLISHED_DATE > '2020-01-01' THEN 'NEW_ARRIVAL'
             WHEN PUBLISHED_DATE < '2000-01-01' THEN 'CLASSIC'
             ELSE 'REGULAR'
        END AS BOOK_CATEGORY
FROM BOOKS;

-- * IDENTIFY THE MOST BORROWED BOOK USING A SUBQUERY.
SELECT BOOK_ID
FROM TRANSACTIONS
GROUP BY BOOK_ID
HAVING COUNT(BOOK_ID) =(
    SELECT MAX(BORROW_COUNT)
    FROM(
        SELECT BOOK_ID, COUNT(BOOK_ID) AS BORROW_COUNT
        FROM TRANSACTIONS
        GROUP BY BOOK_ID
    )
);

